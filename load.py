class Load:
    def __init__(self, length, width, height, weight):
        """
        Инициализация полей класса груза.

        Args:
            length (float): Длина груза.
            width (float): Ширина груза.
            height (float): Высота груза.
            weight (float): Вес груза.
        """
        self.length = length
        self.width = width
        self.height = height
        self.weight = weight